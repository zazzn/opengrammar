#!/usr/bin/env python3
"""Create placeholder PNG icons for the extension"""

# Create simple 1x1 blue pixel PNG files (will be scaled by browser)
# This is a minimal valid PNG format

# 16x16 blue square PNG
icon_16 = bytes([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  # IHDR chunk
    0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10,  # 16x16
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x91, 0x68, 0x36,
    0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, 0x54,  # IDAT chunk
    0x08, 0xD7, 0x63, 0x60, 0x60, 0x60, 0x60, 0x60, 0x60, 0x00, 0x00, 0x00, 0x85, 0x00, 0x01, 0x5D, 0x2D, 0x8A, 0x37,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44,  # IEND chunk
    0xAE, 0x42, 0x60, 0x82
])

# Write icons
with open('icon-16.png', 'wb') as f:
    f.write(icon_16)

with open('icon-48.png', 'wb') as f:
    f.write(icon_16)  # Reuse for now

with open('icon-128.png', 'wb') as f:
    f.write(icon_16)  # Reuse for now

print("Icons created successfully!")
